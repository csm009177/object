// 속성(Property): 객체의 속성은 객체가 가지는 변수나 데이터를 의미합니다. 
// 객체 내의 키(key)와 값(value)으로 구성되며, 이 키는 문자열 또는 심볼이 될 수 있습니다. 
// 속성은 객체의 상태를 나타내며, 점 표기법(objectName.propertyName) 또는 
// 대괄호 표기법(objectName['propertyName'])을 사용하여 접근할 수 있습니다.

const person = {
  name: 'John',
  age: 30
};

console.log(person.name); // 'John'
console.log(person['age']); // 30

